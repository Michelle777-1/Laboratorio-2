def maximo(val1,val2):
    if val1>val2:
        max=val1
    else:
        max=val2
    print("El valor máximo es: ",max)
def minimo(val1,val2):
    if val1<val2:
        min=val1
    else:
        min=val2
    print("El valor mínimo es: ",min)
def iguales(val1,val2):
    print("¿Los números son iguales?")
    return val1==val2
def diferentes(val1,val2):
    print("¿Los números son diferentes?")
    return val1!=val2
